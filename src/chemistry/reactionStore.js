// "Search Online" toggle (Upgrade #10.1): when false, PubChem's live network
// lookup is skipped entirely and resolution falls back to whatever's already
// in the in-memory COMPOUND_BLUEPRINTS table / Supabase cache. Gemini
// generation is unaffected by this toggle — it's a separate, explicitly-opt-in
// AI source.
export let SEARCH_ONLINE_ENABLED = true;

export function setSearchOnlineEnabled(enabled) {
  SEARCH_ONLINE_ENABLED = !!enabled;
}

// Global search generation counter — only the latest generation can commit.
// Exposed via functions (rather than a raw exported `let`) because ES module
// bindings for `let`/`var` are read-only from importing modules, and the
// component needs to increment this on every new inventory search.
let inventorySearchGeneration = 0;

export function getInventorySearchGeneration() {
  return inventorySearchGeneration;
}

export function bumpInventorySearchGeneration() {
  inventorySearchGeneration += 1;
  return inventorySearchGeneration;
}
