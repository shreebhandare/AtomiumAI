// ───────────────────────── SHARED REACTION STATE ─────────────────────────
// This module centralizes the mutable, module-level state that used to live
// directly inside ChemLabCanvas.jsx.
//
// There are two distinct concepts living in this file, and they must never be
// confused with one another:
//
//   COMPOUND_BLUEPRINTS — "how do these raw atoms bond into ONE molecule?"
//     Keyed by a fingerprint of atom symbols (e.g. "H-H-O"). Each entry
//     describes a single molecule's internal atom-to-atom bond graph
//     (which atom index bonds to which, with what order/type). This powers
//     the atom-assembly sandbox: dragging loose H/H/O atoms together and
//     having the engine recognize + bond them into water. PubChem/Gemini/
//     Supabase populate this table. It NEVER describes a reaction between
//     two already-formed molecules.
//
//   REACTIONS — "what happens when these already-formed MOLECULES meet?"
//     The molecule-based reaction database (precipitation, neutralization,
//     combustion, decomposition, synthesis, single/double displacement...).
//     Each entry is keyed by a canonical signature of its reactant molecule
//     FORMULAS (order-independent) and looks like:
//       {
//         reactants: ["BaCl2", "Na2SO4"],
//         products: [
//           { formula: "BaSO4", coefficient: 1 },
//           { formula: "NaCl",  coefficient: 2 },
//         ],
//         name, fact, type, deltaH, ...
//       }
//     Products are ALWAYS looked up here (or generated by prediction) —
//     never synthesized by concatenating/merging reactant atoms together.
//     See reactionEngine.js / moleculeReactionResolver.js for how this is
//     consumed.

import { canonicalReactantSignature } from "./reactionUtils";

// ── Atom-assembly compound blueprints (formerly named REACTIONS) ──
export const COMPOUND_BLUEPRINTS = {};

// ── Molecule-based reaction database (the new, molecule-based REACTIONS) ──
export const REACTIONS = {};

let isReady = false;

export function isReactionStoreReady() {
  return isReady;
}

export function setReactionStoreReady(ready) {
  isReady = !!ready;
}

export function clearReactions() {
  for (const key in REACTIONS) {
    delete REACTIONS[key];
  }
}

/**
 * Registers a molecule-based reaction entry under its canonical reactant
 * signature so the resolver can look it up regardless of the order the
 * reactant molecules were listed/placed in (BaCl2 + Na2SO4 === Na2SO4 + BaCl2).
 */
export function registerReaction(entry) {
  if (!entry || !Array.isArray(entry.reactants) || entry.reactants.length === 0) return;
  const key = canonicalReactantSignature(entry.reactants);
  REACTIONS[key] = entry;
  return key;
}

/**
 * Finds a known atom-bond blueprint for a given product molecule formula
 * (e.g. "NaCl"), if one has already been resolved/cached. Used when the
 * reaction engine needs to spawn a product molecule's atoms+bonds on the
 * canvas — falls back to an async PubChem/Gemini lookup (see
 * findCompoundEntry in reactionResolutionService.js) when nothing is cached.
 */
export function findBlueprintByFormula(formula) {
  if (!formula) return null;
  for (const key in COMPOUND_BLUEPRINTS) {
    if (COMPOUND_BLUEPRINTS[key]?.formula === formula) return COMPOUND_BLUEPRINTS[key];
  }
  return null;
}

